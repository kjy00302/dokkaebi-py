def 합쳐한글(자모):
    return chr(0xac00 + ((자모[0] * 21) + 자모[1]) * 28 + 자모[2])


def 나눠한글(한글):
    조각 = ord(한글) - 0xac00
    초성 = 조각 // (21 * 28)
    중성 = 조각 % (21 * 28) // 28
    종성 = 조각 % 28
    return (초성, 중성, 종성)

    
