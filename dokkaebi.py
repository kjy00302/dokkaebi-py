def 안도깨비말(한글들):
    중성합성사전 = {
        중성사전['ㅗ']: {
            중성사전['ㅏ']: 중성사전['ㅘ'],
            중성사전['ㅐ']: 중성사전['ㅙ'],
            중성사전['ㅣ']: 중성사전['ㅚ']
            },
        중성사전['ㅜ']: {
            중성사전['ㅓ']: 중성사전['ㅝ'],
            중성사전['ㅔ']: 중성사전['ㅞ'],
            중성사전['ㅣ']: 중성사전['ㅟ']
            },
        중성사전['ㅡ']: {
            중성사전['ㅣ']: 중성사전['ㅢ']
            }
    }
    임시 = ''
    셈 = 0
    while 셈 < len(한글들):
        if 44032 <= ord(한글들[셈]) <= 55203:
            임시2다시1 = 나눠한글(한글들[셈])
            임시2다시2 = 나눠한글(한글들[셈 + 1])

            if 임시2다시1[1] in [
                    중성사전['ㅏ'], 중성사전['ㅐ'], 중성사전['ㅓ'],
                    중성사전['ㅔ'], 중성사전['ㅣ'], 중성사전['ㅑ'],
                    중성사전['ㅒ'], 중성사전['ㅕ'], 중성사전['ㅖ'],
                    중성사전['ㅠ'], 중성사전['ㅛ'], 중성사전['ㅘ'],
                    중성사전['ㅙ'], 중성사전['ㅝ'], 중성사전['ㅞ'],
                    중성사전['ㅟ'], 중성사전['ㅚ'], 중성사전['ㅢ']]:
                임시 += (합쳐한글((임시2다시1[0], 임시2다시1[1], 임시2다시2[2])))
                셈 += 2

            elif 임시2다시1[1] in [중성사전['ㅜ'], 중성사전['ㅗ'], 중성사전['ㅡ']]:
                if 임시2다시2[1] in [
                        중성사전['ㅏ'], 중성사전['ㅐ'],
                        중성사전['ㅓ'], 중성사전['ㅔ'], 중성사전['ㅣ']]:
                    임시 += (합쳐한글((
                        임시2다시1[0],
                        중성합성사전[임시2다시1[1]][임시2다시2[1]],
                        임시2다시2[2])))
                else:
                    임시 += (합쳐한글((임시2다시1[0], 임시2다시1[1], 임시2다시2[2])))
                셈 += 2
        else:
            임시 += 한글들[셈]
            셈 += 1
    return 임시
